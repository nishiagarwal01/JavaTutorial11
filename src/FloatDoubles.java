public class FloatDoubles {

    public static void main(String[] args) {
        int intNumber = 5;
        float floatNumber = 5f/3f;
        double doubleNumber = 5d/4d;
        System.out.println("Hello World!");
        System.out.println("myIntValue " + intNumber);
        System.out.println("myFloatValue " + floatNumber);
        System.out.println("myDoubleValue "+ doubleNumber);

        double valueInPounds= 6;
        double valueInKgs = valueInPounds * 0.45359237;
        System.out.println("Value in Kgs "+ valueInKgs);
    }
}
